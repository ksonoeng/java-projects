public class ForLoop {
    public static void main(String [] args) {
        for(int i = 0;i<=10;i++ ){              // i++ == i = i+1  (true)
            System.out.println("Hello "+i);
        }
        for(int i = 10;i>=0; i--){              // i-- == i = i-1  (true)
            System.out.println("Hello "+i);
        }
    }
}
